const PESOS = [3, 7, 1, 3, 7, 1, 3, 7, 1, 3, 7, 1, 3, 7, 1, 3, 7]

export function validateClabe(clabe: string): boolean {
  if (!/^\d{18}$/.test(clabe)) return false

  const digits = clabe.split("").map(Number)
  const sum = PESOS.reduce((acc, peso, i) => acc + (digits[i] * peso) % 10, 0)
  const control = (10 - (sum % 10)) % 10

  return control === digits[17]
}

